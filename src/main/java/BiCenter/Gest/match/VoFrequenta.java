package BiCenter.Gest.match;

public class VoFrequenta {

	private Integer idDest;
	private Integer idCorso;
	/**
	 * @param idDest
	 * @param idCorso
	 */
	public VoFrequenta(Integer idDest, Integer idCorso) {
		super();
		this.idDest = idDest;
		this.idCorso = idCorso;
	}
	public Integer getIdDest() {
		return idDest;
	}
	public void setIdDest(Integer idDest) {
		this.idDest = idDest;
	}
	public Integer getIdCorso() {
		return idCorso;
	}
	public void setIdCorso(Integer idCorso) {
		this.idCorso = idCorso;
	}
	
	
}
